from functools import reduce

def load_puzzle(input):
    lines = input.split('\n')
    puzzle = []
    y = 0
    for line in lines:
        row = []
        x = 0
        for s in line:
            row.append(s)
            x += 1

        puzzle.append(row)
        y += 1

    return puzzle


def find_starting_candidates(puzzle, s, length):
    candidates = dict()
    distance = length - 1
    for y in range(len(puzzle)):
        for x in range(len(puzzle[0])):
            if puzzle[y][x] == s:
                possible_directions = [1,1,1,1,1,1,1,1]  # could optimise with bitflags
                if y - distance < 0:
                    possible_directions[7] = 0
                    possible_directions[0] = 0
                    possible_directions[1] = 0

                if x + distance >= len(puzzle[0]):
                    possible_directions[1] = 0
                    possible_directions[2] = 0
                    possible_directions[3] = 0
                
                if y + distance >= len(puzzle):
                    possible_directions[3] = 0
                    possible_directions[4] = 0
                    possible_directions[5] = 0
                
                if x - distance < 0:
                    possible_directions[5] = 0
                    possible_directions[6] = 0
                    possible_directions[7] = 0

                candidates[(x, y)] = possible_directions

    return candidates


def update_candidates(puzzle, candidates, s, distance):
    inactive_candidates = []
    for candidate in candidates.keys():
        x = candidate[0]
        y = candidate[1]

        directions = candidates[candidate]

        active = False
        if directions[0] != 0:
            if puzzle[y - distance][x] == s:
                active = True
            else:
                directions[0] = 0

        if directions[1] != 0:
            if puzzle[y - distance][x + distance] == s:
                active = True
            else:
                directions[1] = 0

        if directions[2] != 0:
            if puzzle[y][x + distance] == s:
                active = True
            else:
                directions[2] = 0

        if directions[3] !=0:
            if puzzle[y + distance][x + distance] == s:
                active = True
            else:
                directions[3] = 0

        if directions[4] != 0:
            if puzzle[y + distance][x] == s:
                active = True
            else:
                directions[4] = 0

        if directions[5] != 0:
            if puzzle[y + distance][x - distance] == s:
                active = True
            else:
                directions[5] = 0

        if directions[6] != 0:
            if puzzle[y][x - distance] == s:
                active = True
            else:
                directions[6] = 0

        if directions[7] != 0:
            if puzzle[y - distance][x - distance] == s:
                active = True
            else:
                directions[7] = 0

        if not active:
            inactive_candidates.append(candidate)

    for inactive in inactive_candidates:
        del candidates[inactive]
    
    return candidates



